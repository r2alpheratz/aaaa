package com.sds.holidayCalenadar;

public class Holiday {
	String name;
	String description;
	String strtDate;
	String endDate;
	
	public Holiday() {
		super();
	}

	public Holiday(String name, String description, String strtDate,
			String endDate) {
		super();
		this.name = name;
		this.description = description;
		this.strtDate = strtDate;
		this.endDate = endDate;
	}
	
}
