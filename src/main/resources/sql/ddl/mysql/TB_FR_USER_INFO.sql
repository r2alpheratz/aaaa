CREATE TABLE IF NOT EXISTS TB_FR_USER_INFO (
  USER_SQ VARCHAR(10) NOT NULL,
  USER_ID VARCHAR(20) NOT NULL,
  USER_NM VARCHAR(50) NOT NULL,
  PASSWORD VARCHAR(128)  NULL,
  EMAIL VARCHAR(100) NULL,
  PW_FAIL_CNT INT(1) NULL,
  PHONE_NUMBER VARCHAR(18) NULL,
  RECENT_LOGIN_DT DATETIME NULL,
  RECENT_LOGIN_IP VARCHAR(30) NULL,
  GRD_CD VARCHAR(10) NULL,
  STATUS_CD VARCHAR(10) NOT NULL,
  CREATE_DT DATETIME NULL,
  CREATE_USR_ID VARCHAR(20) NULL,
  UPDATE_DT DATETIME NULL,
  UPDATE_USR_ID VARCHAR(20) NULL,
  PW_CHANGE_DT DATETIME NULL,
  PRIMARY KEY (USER_SQ),
  UNIQUE KEY (USER_ID),
  FOREIGN KEY (GRD_CD) REFERENCES TB_FR_CODE_INFO(CODE_SQ) ,
  FOREIGN KEY (STATUS_CD) REFERENCES TB_FR_CODE_INFO(CODE_SQ)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;